&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	中新网北京10月13日电(上官云)2016年诺贝尔文学奖即将公布。近日，&lt;a href=&quot;http://country.huanqiu.com/united_kingdom&quot; class=&quot;linkAbout&quot; target=&quot;_blank&quot;&gt;英国&lt;/a&gt;博彩公司“立博”(Ladbrokes)开出了相关赔率榜，预测今年得主。除多年来一直位居前列的&lt;a href=&quot;http://country.huanqiu.com/japan&quot; class=&quot;linkAbout&quot; target=&quot;_blank&quot;&gt;日本&lt;/a&gt;知名作家村上春树之外，还有&lt;a href=&quot;http://country.huanqiu.com/kenya&quot; class=&quot;linkAbout&quot; target=&quot;_blank&quot;&gt;肯尼亚&lt;/a&gt;作家恩古吉·瓦·提安哥等人。
&lt;/p&gt;
&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	&lt;strong&gt;赔率榜上仍然“吸睛”的村上春树&lt;/strong&gt;
&lt;/p&gt;
&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	在Ladbrokes和unibet两家著名博彩网站上，今年日本作家村上春树的赔率依然排在前列。但人们对这个情形应该已经习以为常：年年上榜，年年“陪跑”。甚至有读者戏称，村上春树已经成了博彩公司吸引眼球的“噱头”。
&lt;/p&gt;
&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	不过，由于近几年，赔率榜排名或排名变化与当年诺贝尔文学奖得主有一定的同步性，不少人仍然认为，村上春树具有较高的获奖概率。
&lt;/p&gt;
&lt;p align=&quot;center&quot; style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	&lt;img src=&quot;http://himg2.huanqiu.com/attachment2010/2016/1013/20161013071335910.jpg&quot; style=&quot;height:auto;&quot; /&gt;
&lt;/p&gt;
&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	资料图：1948年出生的阿列克谢耶维奇，毕业于明斯克大学新闻学系。她用与当事人访谈的方式写作纪实文学，记录了二次世界大战等人类历史上重大的事件。
&lt;/p&gt;
&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	但评论家白烨并不看好村上春树能够获奖。他在接受中新网(微信公众号：cns2012)记者采访时曾指出，村上春树得诺奖几乎没希望，“诺贝尔文学奖很重视作品的文学价值，村上春树的作品对现实社会的关注度并不高，在纯文学圈子里不是特别受到认可”。
&lt;/p&gt;
&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	评论家止庵等人亦曾分析过，诺贝尔文学奖会给一些有思考的冷门作家以鼓励，也重视作品的文学价值。在白烨看来，村上春树的作品具有流行性、大众性的特点，“正是从这个角度讲，村上很有可能成为一个时间悠久的‘陪跑者’”。
&lt;/p&gt;
&lt;p style=&quot;font-size:16px;font-family:SimSun;color:#2B2B2B;&quot;&gt;
	“村上春树得诺奖的希望不大了。”基于相似原因，村上春树作品的译者之一林少华表示。
